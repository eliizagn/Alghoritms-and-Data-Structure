#include <iostream>

using namespace std;

void SiftDown(int* a, int n, int i) {
    int smallest = i;
    int left = 2 * i + 1;
    int right = 2 * i + 2;

    if (left < n && a[left] < a[smallest]) {
        smallest = left;
    }

    if (right < n && a[right] < a[smallest]) {
        smallest = right;
    }

    if (smallest != i) {
        swap(a[i], a[smallest]);
        SiftDown(a, n, smallest);
    }
}

void heapSort(int* a, int n) {
    for (int i = n / 2 - 1; i >= 0; i--) {
        SiftDown(a, n, i);
    }

    for (int i = n - 1; i > 0; i--) {
        swap(a[0], a[i]);
        SiftDown(a, i, 0);
    }
}

int main() {
    int n;
    cin >> n;

    int* a = new int[n];

    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    heapSort(a, n);

    for (int i = n - 1; i >= 0; i--) {
        cout << a[i] << " ";
    }

    cout << endl;

    delete[] a;

    return 0;
}

