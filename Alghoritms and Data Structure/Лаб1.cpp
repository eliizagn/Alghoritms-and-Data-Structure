#include <iostream>
#include <string>

using namespace std;

bool Palindrome(const string& m, int left, int right) {
    while (left < right) {
        if (m[left] != m[right]) {
            return false;
        }
        left++;
        right--;
    }
    return true;
}

bool check(const string& m, int n) {
    int left = 0;
    int right = n - 1;

    while (left < right) {
        if (m[left] != m[right]) {
            return Palindrome(m, left + 1, right) || Palindrome(m, left, right - 1);
        }
        left++;
        right--;
    }

    return true;
}

int main() {
    int n;
    cin >> n;

    string word;
    cin >> word;

    if (n == word.length() && check(word, n)) {
        cout << "YES" << endl;
    }
    else {
        cout << "NO" << endl;
    }

    return 0;
}


